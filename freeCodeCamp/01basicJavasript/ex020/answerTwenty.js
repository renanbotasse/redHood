/* % é difrente de /
% é utilziado para sabermos o número remanescente da operação, exemplo
85 % 10 = 5 pois cabem 8 vezes de 10 dentro de 85 e sobram 5; 
Já /, no caso, 85 / 10 = 8,5

% pode ser utilziado para saber se um número é odd (impar) ou even (par) */